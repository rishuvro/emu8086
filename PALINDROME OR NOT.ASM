DATA SEGMENT
MSG1 DB 10,13,'ENTER THE STRING: $'
MSG2 DB 10,13,'STRING IS PALINDROME$'
MSG3 DB 10,13,'STRING IS NOT PALINDROME$'
NEW DB 10,13,'$'
INST DB 20 DUP(0)
DATA ENDS
CODE SEGMENT              
ASSUME CS:CODE,DS:DATA  

START:
MOV AX,DATA
MOV DS,AX
LEA DX,MSG1
MOV AH,09H
INT 21H
MOV BX,00

L1:
MOV AH,01H
INT 21H
CMP AL,0DH
JE L2
MOV [INST+BX],AL
INC BX
LOOP L1 

L2: 
MOV DI,0
DEC BX
JNZ L3

L3:
MOV AL,[INST+BX]
CMP AL,[INST+DI]
JNE L4
INC DI
DEC BX
JNZ L3 
LEA DX,NEW
MOV AH,09H
INT 21H
LEA DX,MSG2
MOV AH,09H
INT 21H
JMP RESULT 


L4:
LEA DX,MSG3
MOV AH,09H
INT 21H

RESULT:
INT 3
CODE ENDS
END START
END
